"""
Presenters — чистые функции без сети/побочных эффектов: домен -> (текст,
клавиатура). Отдельный слой специально ради тестируемости без мока сети
и без реального токена бота.
"""
from typing import Optional, Tuple

from telebot import types


def build_welcome_message() -> Tuple[str, Optional[types.InlineKeyboardMarkup]]:
    text = (
        "👋 Привет! Это официальный Telegram-бот клуба «Мафия Style».\n\n"
        "Привязка аккаунта скоро появится на сайте (кнопка «Войти через "
        "Telegram» в профиле) — как только это будет готово, здесь "
        "появятся профиль, статистика, рейтинг, турниры и уведомления.\n\n"
        "Пока что бот в разработке — заходите позже."
    )
    return text, None
